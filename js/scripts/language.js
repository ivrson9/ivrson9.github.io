/**
* setLanguage
* use $.lang[currentLanguage][languageNumber]
*/
function setLanguage(currentLanguage) {
	if(!currentLanguage){
		var userLang = navigator.language || navigator.userLanguage;
		if(userLang == 'ko' || 'ko-kr') currentLanguage = 'ko';
		else if(userLang == 'de' || 'de-de') currentLanguage = 'de';
		else currentLanguage = 'en';
	}

	$('[data-langNum]').each(function() {
		var $this = $(this);
		$this.html($.lang[currentLanguage][$this.data('langnum')]);
	});

	// placeholder
	$('#name').attr('placeholder', $.lang[currentLanguage][42]);
	$('#email').attr('placeholder', $.lang[currentLanguage][43]);
	$('#message').attr('placeholder', $.lang[currentLanguage][44]);

	// set cookie
	var past = $.cookie('customLocale');

	if (past != currentLanguage){
		$.removeCookie('customLocale', {path: '/'});
		$.cookie('customLocale', currentLanguage, {expires: 365, path: '/'});
	}
}

// 언어 변경
$('.language').click(function() {
	var lang = $(this).data('lang');
	setLanguage(lang);

	$(this).css('color', '#fff !important');
	//event.preventDefault(); // href 중지
});

